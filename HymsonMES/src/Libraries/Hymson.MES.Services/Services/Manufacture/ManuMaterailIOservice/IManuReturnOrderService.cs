        /// 查询详情（生产退料表）
        /// </summary>
        /// <param name="returnId"></param>
        /// <returns></returns>
        Task<IEnumerable<ManuReturnOrderDetailDto>> QueryDetailByReturnIdAsync(long returnId);

    }
}