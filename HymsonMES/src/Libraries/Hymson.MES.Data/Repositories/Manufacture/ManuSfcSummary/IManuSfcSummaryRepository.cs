        /// 分页查询
        /// </summary>
        /// <param name="pagedQuery"></param>
        /// <returns></returns>
        Task<PagedInfo<ManuSfcSummaryEntity>> GetPagedListAsync(ManuSfcSummaryPagedQuery pagedQuery);

    }
}
